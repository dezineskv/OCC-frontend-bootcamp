// arithmetic calculation
let a = 10;
let b = 5;

//  addition
let sum = a + b;
console.log("This is the sum of a and b:", sum);

// subtraction
let difference = a - b;
console.log("This is the difference for a and b:", difference);

// multiplication
let product = a * b;
console.log("This is the product of a and b:", product);

// division
let division = a / b;
console.log("This is the division of a and b:", division);

// any number divided by 0 results in Infinity
let x = a / 0;
console.log("The result is", x)

// subtract from a higher #
let y = a - 20;
console.log("The result is", y)

// divide
let z = a / 20;
console.log("The result is", z)

// quotient
let w = 12 % 5;
console.log("The result is", w)

