// logical expressions
let sunny = true;
let warm = false;
//checking if both of them is true

// true && true = true

// false && true = false

// true && false = false

// false && false = false

console.log("Is it sunny and warm?", sunny && warm);
console.log("Is it sunny OR warm?", sunny || warm);
//checking if one of them is true
// true | | true = true

// false | | true = true

// true | | false = true

// false | | false = false



